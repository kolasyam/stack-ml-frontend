import * as React from 'react';
import { cn } from '@/lib/utils';

/**
 * Card — 20px pill-corner surface with a hairline border (Verge elevation is
 * colour, not shadow). `interactive` adds a subtle hover lift + mint focus ring.
 */
export const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { interactive?: boolean }
>(({ className, interactive, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      'rounded-card border border-fg/10 bg-surface/60 backdrop-blur-sm',
      interactive &&
        'transition-all duration-200 ease-verge hover:border-mint/40 hover:bg-surface/80 focus-within:border-mint/40',
      className,
    )}
    {...props}
  />
));
Card.displayName = 'Card';

